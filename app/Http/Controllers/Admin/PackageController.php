     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        // return $request;
        $request->validate([
            'title' => 'required',
            'price' => 'required',
            'validity' => 'required',
        ]);

        $package = Package::find($id);
        // return $package;
        // return $request->validity;
        $package->update([
            'title' => $request->title,
            'price' => $request->price,
            'validity' => $request->validity,
        ]);

        return redirect()->route('package.index')->with(['status' => true,'message' => 'Updated Successfully']);
    }

